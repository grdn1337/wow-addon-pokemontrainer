local AddonName, PT = ...;
local _G = _G;

local module = PT:NewModule("_Data");

local AuraStates;

local _as = {};
function module:GetAuraStates(player)
	_G.wipe(_as);
	
	-- quickly catching weather before
	local id = (_G.C_PetBattles.GetAuraInfo(PT.WEATHER, PT.PAD_INDEX, 1)); -- force one value
	if( id and AuraStates[id] ) then
		for state = 1, #AuraStates[id] do
			table.insert(_as, AuraStates[id][i]);
		end
	end
	
	for pad = PT.PAD_INDEX, player.numPets do
		for aura = 1, _G.C_PetBattles.GetNumAuras(player.side, pad) do
			id = (_G.C_PetBattles.GetAuraInfo(player.side, pad, aura)); -- force one value
			if( id and AuraStates[id] ) then
				for state = 1, #AuraStates[id] do
					table.insert(_as, AuraStates[id][i]);
				end
			end
		end
	end
	
	return _as;
end

